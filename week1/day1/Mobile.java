package week1.day1;

public class Mobile {
	
	public static void main(String[] args) {
		
		/*
		 * int, boolean, float, character
		 */
		
		
		/*
		 * Syntax of the variable
		 * variable name should start with lowercase
		 * data_type variable_name = value;
		 */
		
		int price = 20000;  // int --> no quotes
		boolean isAndroid = true; // boolean --> no quotes
		float osVersion = 11.31f;  // float --> no quotes
		char logo = 'B'; // char --> single quote
		String brand = "OnePlus"; // String --> double quotes
		
		System.out.println(price);
		System.out.println(isAndroid);
		System.out.println(osVersion);
		System.out.println(logo);
		System.out.println(brand);

		
	}

}
